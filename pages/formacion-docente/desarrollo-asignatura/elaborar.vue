        las competencias y los resultados de aprendizaje. El docente estima el tiempo que utilizarán
        los estudiantes en el aula y fuera de esta para el desarrollo de las actividades propuestas en
        relación al número de créditos asignados.</p>
      <h3>Apoyo al estudiante</h3>
      <p>Este documento es un apoyo fundamental para los estudiantes que cursan las asignaturas ya que conocen
        a detalle la planificación y las actividades a desarrollar.</p>
      <p>El Plan Docente es publicado en el Entorno Virtual de Aprendizaje de cada asignatura, con la finalidad
        de que los estudiantes puedan revisar las competencias a desarrollar, resultados de aprendizaje,contenidos,
        actividades, indicadores e instrumentos de evaluación y fechas importantes. De esta manera, el
        estudiante puede prepararse previo el inicio de cada jornada de clase.</p>
